const generateAddress = (): string => {
  const city = getRandomCity()
  const street = randomFromArray(popularStreets)
  const isVillage = /с\.|с-ще/.test(city.admin4)
  const houseNumber = randomIntFromInterval(0, isVillage ? 40 : 120)
  const block = isVillage ? '' : getWithProbability(blocks, blocksProbabilities)
  const apartment = isVillage ? '' : randomIntFromInterval(1, 120)
  const address = [
    city.postalCode,
    city.admin1 + ' обл.',
    city.admin2 + ' р-н.',
    city.admin4,
    street,
    houseNumber + block,
    apartment
  ].filter(Boolean).join(', ')
  return address
}

export default generateAddress
